#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	int		size;
	char	*result;

	i = 0;
	size = ft_strlen(s);
	result = ft_strnew(size);
	if (result == NULL)
		return (NULL);
	while (i < size)
	{
		result[i] = f(s[i]);
		i++;
	}
	return (result);
}
