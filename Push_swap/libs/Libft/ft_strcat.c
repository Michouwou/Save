#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	size_t	tmp;

	tmp = ft_strlen(s1);
	i = 0;
	while (i <= ft_strlen(s2))
	{
		s1[tmp + i] = s2[i];
		i++;
	}
	return (s1);
}
