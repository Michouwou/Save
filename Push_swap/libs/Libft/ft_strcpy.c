#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int	i;
	int	size;

	size = ft_strlen(src);
	i = 0;
	while (i <= size)
	{
		dst[i] = src[i];
		i++;
	}
	return (dst);
}
