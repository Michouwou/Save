#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char *tmp;

	tmp = (char*)s;
	if (n != 0)
	{
		while (n-- != 0)
		{
			if (*tmp == (char)c)
				return ((void*)tmp);
			tmp++;
		}
	}
	return (NULL);
}
