#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*tmp;

	tmp = (char*)b;
	i = 0;
	while (i < (int)len)
	{
		tmp[i] = (char)c;
		i++;
	}
	return (b);
}
